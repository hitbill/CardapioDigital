    private void jButtonLoginEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginEnterActionPerformed
        if((jTextFieldLoginUsuer.getText().equalsIgnoreCase("admin") == true)&&(new String(jPasswordFieldLoginPassword.getPassword()).equals("admin"))) {
                CadastrosJFrame janelaCadastros = new CadastrosJFrame();
                janelaCadastros.setVisible(true);
                setVisible(false);
                if(jCheckBoxRemember.isSelected() == false){
                    jTextFieldLoginUsuer.setText(null);
                    jPasswordFieldLoginPassword.setText(null);
                }
        }
        else{
            JOptionPane.showMessageDialog(null,"Usu치rio ou Senha inv치lidos.","Login",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButtonLoginEnterActionPerformed

